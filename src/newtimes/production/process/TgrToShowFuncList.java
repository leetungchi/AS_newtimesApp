public class TgrToShowFuncList extends CommandTrigger {
  public TgrToShowFuncList() {
  }
  protected void activateThisProcess() throws processhandler.commands.CommandException {
    /**@todo Implement this processhandler.CommandTrigger abstract method*/
    if(commander==null)
      throw new CommandException("commander is null");
    if(commander instanceof AbleToShowFuncList  ){
       try{
         ((AbleToShowFuncList)commander).toShowFuncList();
         ctnCtl.recordProcess(this);
       }catch(Exception e){
          e.printStackTrace();
          throw new CommandException(e.getMessage());
       }
    }else{
      throw new CommandException("commander is not able to generate GUI for Function list of Production maintain");
    }

  }

}